#!/usr/bin/env node
/**
 * Fetches GHL course share URLs + portal base URL via the GHL REST API
 * and writes them to marketing/members/ghl-config.js.
 *
 * Usage:
 *   GHL_API_KEY=pit-xxx node server/src/tools/fetch-ghl-config.js
 *
 * Or with the key already in server/.env:
 *   node -r dotenv/config server/src/tools/fetch-ghl-config.js dotenv_config_path=server/.env
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, '../../../');
const CONFIG_PATH = path.join(REPO_ROOT, 'marketing/members/ghl-config.js');

const LOCATION_ID = 'c3HSS74ILjGye3pvGsHg';
const API_BASE = 'https://services.leadconnectorhq.com';
const API_VERSION = '2021-07-28';

// Course name → config key map (matches IA_GHL_CONFIG.courses keys)
const NAME_TO_KEY = [
  { match: /ai side income/i,             key: 'ai' },
  { match: /affiliate/i,                  key: 'affiliate' },
  { match: /estate sale|garage sale/i,    key: 'estate' },
  { match: /bookkeeping/i,                key: 'bookkeeping' },
];

const API_KEY = process.env.GHL_API_KEY;
if (!API_KEY) {
  console.error('✗ GHL_API_KEY not set. Run with:');
  console.error('  GHL_API_KEY=pit-xxx node server/src/tools/fetch-ghl-config.js');
  process.exit(1);
}

async function apiGet(endpoint) {
  const res = await fetch(`${API_BASE}${endpoint}`, {
    headers: {
      Authorization: `Bearer ${API_KEY}`,
      Version: API_VERSION,
      Accept: 'application/json',
    },
  });
  return { status: res.status, body: await res.text() };
}

async function listCourses() {
  // Try several endpoints — GHL's API surface for courses has shifted.
  const candidates = [
    `/courses/?locationId=${LOCATION_ID}&limit=100`,
    `/products/?locationId=${LOCATION_ID}&limit=100`,
    `/memberships/courses/?locationId=${LOCATION_ID}&limit=100`,
  ];
  for (const ep of candidates) {
    const r = await apiGet(ep);
    console.log(`  GET ${ep} → ${r.status}`);
    if (r.status !== 200) continue;
    try {
      const data = JSON.parse(r.body);
      const arr = data.products || data.courses || data.data || data;
      if (Array.isArray(arr) && arr.length > 0) return arr;
    } catch {}
  }
  return [];
}

async function getLocation() {
  const r = await apiGet(`/locations/${LOCATION_ID}`);
  if (r.status !== 200) return null;
  try {
    return JSON.parse(r.body).location || JSON.parse(r.body);
  } catch {
    return null;
  }
}

function pickCourseUrl(course, portalBase) {
  // Try direct URL fields the API may surface
  const candidates = [
    course.shareUrl,
    course.share_url,
    course.publicUrl,
    course.public_url,
    course.url,
  ].filter(Boolean);
  if (candidates.length) return candidates[0];

  // Fall back to portalBase + slug or id
  const id = course.id || course._id || course.productId;
  const slug = course.slug || course.urlSlug || id;
  if (portalBase && slug) {
    return `${portalBase.replace(/\/$/, '')}/products/${slug}`;
  }
  return null;
}

function pickPortalBase(location) {
  if (!location) return 'https://portal.incomeacademy.biz';
  const fields = [
    location.clientPortalDomain,
    location.client_portal_domain,
    location.portalDomain,
    location.customDomain,
    location.domain,
  ].filter(Boolean);
  if (fields.length) {
    const d = fields[0].startsWith('http') ? fields[0] : `https://${fields[0]}`;
    return d.replace(/\/$/, '');
  }
  return 'https://portal.incomeacademy.biz';
}

(async () => {
  console.log('→ Fetching GHL courses + portal base...');
  const [location, courses] = await Promise.all([getLocation(), listCourses()]);

  if (!courses.length) {
    console.error('✗ Could not fetch courses. Check GHL_API_KEY scopes (needs courses:read or products:read).');
    process.exit(2);
  }
  console.log(`  Found ${courses.length} courses/products`);

  const portalBase = pickPortalBase(location);
  console.log(`  Portal base: ${portalBase}`);

  const resolved = { ai: null, affiliate: null, estate: null, bookkeeping: null };
  for (const course of courses) {
    const name = course.name || course.title || '';
    const map = NAME_TO_KEY.find((m) => m.match.test(name));
    if (!map) continue;
    const url = pickCourseUrl(course, portalBase);
    if (url && !resolved[map.key]) {
      resolved[map.key] = url;
      console.log(`  ✓ ${map.key.padEnd(12)} → ${url}`);
    }
  }

  const missing = Object.entries(resolved).filter(([, v]) => !v).map(([k]) => k);
  if (missing.length) {
    console.warn(`  ⚠ Missing URLs for: ${missing.join(', ')} — left as placeholders.`);
  }

  const out = `// Income Academy — Member dashboard URL config
// AUTO-GENERATED by server/src/tools/fetch-ghl-config.js
// Last run: ${new Date().toISOString()}
//
// To re-fetch: GHL_API_KEY=pit-xxx node server/src/tools/fetch-ghl-config.js

window.IA_GHL_CONFIG = {
  portalBase: ${JSON.stringify(portalBase)},
  courses: {
    ai:           ${JSON.stringify(resolved.ai || 'GHL_COURSE_URL_AI')},
    affiliate:    ${JSON.stringify(resolved.affiliate || 'GHL_COURSE_URL_AFFILIATE')},
    estate:       ${JSON.stringify(resolved.estate || 'GHL_COURSE_URL_ESTATE')},
    bookkeeping:  ${JSON.stringify(resolved.bookkeeping || 'GHL_COURSE_URL_BOOKKEEPING')},
  },
  generatedAt: ${JSON.stringify(new Date().toISOString())},
};
`;

  fs.writeFileSync(CONFIG_PATH, out, 'utf8');
  console.log(`✓ Wrote ${path.relative(REPO_ROOT, CONFIG_PATH)}`);
})();
